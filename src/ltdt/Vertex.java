package ltdt;

public class Vertex {
	private String name;
	private int index;

	public Vertex(String name) {
		super();
		this.name = name;
	}

	public int getIndex() {
		return index;
	}

	public void setIndex(int index) {
		this.index = index;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
